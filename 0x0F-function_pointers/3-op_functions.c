#include <stdlib.h>
#include "3-calc.h"
/**
 * op_add - function to add tow int numbers
 * @a: first number
 * @b: second number
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function to get difference of two int numbers
 * @a: first number
 * @b: second number
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function to multiply two in numbers
 * @a: first number
 * @b: second number
 * Return: multipliy
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function to divide two numbers
 * @a: first number
 * @b: second number
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function to find modulos of two numbers
 * @a: first number
 * @b: second number
 * Return: modulo of ints
 */

int op_mod(int a, int b)
{
	return (a % b);
}
